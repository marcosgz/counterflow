defmodule Counterflow.Release do
  @moduledoc """
  Tasks invoked from the release scripts (rel/overlays/bin/migrate).
  Equivalent of `mix ecto.migrate` but runnable inside a release where
  Mix is not available.
  """

  @app :counterflow

  def migrate do
    load_app()

    for repo <- repos() do
      {:ok, _, _} = Ecto.Migrator.with_repo(repo, &Ecto.Migrator.run(&1, :up, all: true))
    end
  end

  def rollback(repo, version) do
    load_app()
    {:ok, _, _} = Ecto.Migrator.with_repo(repo, &Ecto.Migrator.run(&1, :down, to: version))
  end

  defp repos do
    Application.fetch_env!(@app, :ecto_repos)
  end

  defp load_app do
    Application.load(@app)
  end
end
